/**
 * A simple 2D vector with functions for performing mathematical operations.
 */
export class Vector2 {
    x: number;
    y: number;

    /**
     * Creates a 2D vector.
     * @param x The x component
     * @param y The y component
     */
    constructor(x: number, y: number) {
        this.x = x;
        this.y = y;
    }
}
